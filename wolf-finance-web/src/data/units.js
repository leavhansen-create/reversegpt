export function getBiomeUnits(biome) {
  return units.filter((u) => u.biome === biome);
}

export function getUnitById(id) {
  return units.find((u) => u.id === id);
}

export function getWolfStageForCompletedUnits(completedCount) {
  const stages = [...WOLF_STAGES].reverse();
  const stage = stages.find((s) => completedCount >= s.unlocksAtUnit - 1);
  return stage ?? WOLF_STAGES[0];
}
